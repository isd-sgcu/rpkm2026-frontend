import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import ts from "typescript";

const frontendRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const backendRoot = resolve(frontendRoot, "../fdrpkm2026-backend");
const configPath = resolve(backendRoot, "tsconfig.json");
const appPath = resolve(backendRoot, "src/app.ts");
const outputPath = resolve(frontendRoot, "src/lib/backend.d.ts");

const formatHost = {
  getCanonicalFileName: (path) => path,
  getCurrentDirectory: () => frontendRoot,
  getNewLine: () => ts.sys.newLine,
};

const failOnErrors = (diagnostics) => {
  const errors = diagnostics.filter(
    ({ category }) => category === ts.DiagnosticCategory.Error,
  );

  if (errors.length > 0) {
    throw new Error(
      ts.formatDiagnosticsWithColorAndContext(errors, formatHost),
    );
  }
};

const config = ts.readConfigFile(configPath, ts.sys.readFile);
failOnErrors(config.error ? [config.error] : []);

const parsed = ts.parseJsonConfigFileContent(
  config.config,
  ts.sys,
  backendRoot,
  {
    declaration: true,
    declarationMap: false,
    emitDeclarationOnly: true,
    noEmit: false,
  },
  configPath,
);
failOnErrors(parsed.errors);

const program = ts.createProgram(parsed.fileNames, parsed.options);
const app = program.getSourceFile(appPath);

if (!app) {
  throw new Error(`Backend entrypoint not found: ${appPath}`);
}

failOnErrors([
  ...program.getSyntacticDiagnostics(app),
  ...program.getSemanticDiagnostics(app),
]);

let declaration;
const emit = program.emit(
  app,
  (_fileName, content) => {
    declaration = content;
  },
  undefined,
  true,
);
failOnErrors(emit.diagnostics);

if (!declaration) {
  throw new Error(`TypeScript did not emit a declaration for ${appPath}`);
}

const banner =
  "// Generated by `bun run api:types` from ../fdrpkm2026-backend/src/app.ts.\n" +
  "// Do not edit this file manually.\n\n";

ts.sys.writeFile(outputPath, banner + declaration);
console.log(`Wrote ${outputPath}`);
